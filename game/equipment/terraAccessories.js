function getAllAccessoriesList() {
	let all_accessories_list = Object.keys(setup.terraAccessories);
	return all_accessories_list;
}
window.getAllAccessoriesList = getAllAccessoriesList

function getAccessoriesObtained() {
	for (let accessory in setup.terraAccessories) {
        setup.terraAccessories[accessory].obtained = V[accessory];
    }
    let accessory_obtained = Object.keys(setup.terraAccessories);
    return accessory_obtained.filter(accessory => {
        return setup.terraAccessories[accessory].obtained === true;
    });
}
window.getAccessoriesObtained = getAccessoriesObtained;