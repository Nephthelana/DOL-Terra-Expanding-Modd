function getAllMaterialList() {
	for (let material in setup.terraMaterial) {
        setup.terraMaterial[material].amount = V[material] || 0;
    }
    let all_material_list = Object.keys(setup.terraMaterial);
	return all_material_list;
}
window.getAllMaterialList = getAllMaterialList

function getMaterialObtainedList() {
	for (let material in setup.terraMaterial) {
        setup.terraMaterial[material].amount = V[material] || 0;
    }
    let material_obtained = Object.keys(setup.terraMaterial);
    return material_obtained.filter(material => {
        return setup.terraMaterial[material].amount > 0 || setup.terraMaterial[material].obtained === true;
    });
}
window.getMaterialObtainedList = getMaterialObtainedList;